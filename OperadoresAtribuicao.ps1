# Operadores de Atribuição
# Atribuição simples

$variavel = 10
Write-Host "Valor da variável: $variavel"

# Atribuição de soma
$variavel += 5
Write-Host "Valor da variável: $variavel"

# Atribuição de subtração
$variavel -= 3
Write-Host "Valor da variável: $variavel"

# Atribuição de multiplicação
$variavel *= 2
Write-Host "Valor da variável: $variavel"

# Atribuição de divisão
$variavel /= 4
Write-Host "Valor da variável: $variavel"

# Atribuição de resto
$variavel %= 3
Write-Host "Valor da variável: $variavel"

